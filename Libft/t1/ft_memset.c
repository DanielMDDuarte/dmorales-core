#include <stddef.h>

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned int	i;
	char			*t;

	i = 0;
	t = s;
	while (i < n)
	{
		*t = c;
		t++;
		i++;
	}
	return (s);
}
